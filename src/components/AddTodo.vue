<template>
  <div>
    <form v-on:submit="addTodo">
      <input v-model="title" type="text" name="title" placeholder="Add todo..." />
      <input class="btn" type="submit" value="Submit" />
    </form>
  </div>
</template>

<script>
export default {
  name: "AddTodo",
  data() {
    return {
      title: ""
    };
  },
  methods: {
    addTodo(e) {
      e.preventDefault();
      const newTodo = {
        id: Math.floor(Math.random() * 999) + 1,
        title: this.title,
        completed: false
      };
      // Send to parent
      this.$emit("add-todo", newTodo);
      // Clear input after submit
      this.title = "";
    }
  }
};
</script>

<style scoped>
form {
  display: flex;
}
input[type="text"] {
  flex: 10;
  padding: 5px;
}
input[type="submit"] {
  flex: 2;
}
</style>